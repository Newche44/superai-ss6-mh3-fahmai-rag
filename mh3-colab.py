def dense_retrieve(query, k=TOP_K_RETRIEVE):
    q_emb = embed_model.encode([query], normalize_embeddings=True)
    scores = (chunk_embeddings @ q_emb.T).flatten()
    top_idx = np.argsort(scores)[::-1][:k]
    return top_idx, scores[top_idx]

def bm25_retrieve(query, k=TOP_K_RETRIEVE):
    tokens = word_tokenize(query, engine="newmm")
    scores = bm25.get_scores(tokens)
    top_idx = np.argsort(scores)[::-1][:k]
    return top_idx, scores[top_idx]

def hybrid_retrieve(query, k=TOP_K_FINAL, rrf_k=RRF_K):
    dense_idx, _ = dense_retrieve(query, k=TOP_K_RETRIEVE)
    bm25_idx, _ = bm25_retrieve(query, k=TOP_K_RETRIEVE)
    rrf = defaultdict(float)
    for rank, idx in enumerate(dense_idx):
        rrf[idx] += 1.0 / (rrf_k + rank + 1)
    for rank, idx in enumerate(bm25_idx):
        rrf[idx] += 1.0 / (rrf_k + rank + 1)
    sorted_idx = sorted(rrf.keys(), key=lambda x: rrf[x], reverse=True)
    return sorted_idx[:k], [rrf[i] for i in sorted_idx[:k]]

def retrieve_for_comparison(query, product_names, k_per_product=3):
    seen = set()
    retrieved = []
    h_idx, _ = hybrid_retrieve(query, k=TOP_K_FINAL)
    for idx in h_idx:
        seen.add(idx)
        retrieved.append(all_chunks[idx])
    for pname in product_names:
        tokens = word_tokenize(pname, engine="newmm")
        scores = bm25.get_scores(tokens)
        top = np.argsort(scores)[::-1][:k_per_product]
        for idx in top:
            if idx not in seen and scores[idx] > 0:
                seen.add(idx)
                retrieved.append(all_chunks[idx])
    return retrieved

