def _call_llm_api(messages, model, max_retries=5, temperature=0):
    url = f"http://thaillm.or.th/api/{model}/v1/chat/completions"
    headers = {"Content-Type": "application/json", "apikey": THAILLM_API_KEY}
    payload = {"model": "/model", "messages": messages,
               "max_tokens": 2048, "temperature": temperature}
    for attempt in range(max_retries):
        try:
            resp = requests.post(url, headers=headers, json=payload, timeout=180)
            if resp.status_code == 429:
                time.sleep(min(2 ** attempt, 30))
                continue
            resp.raise_for_status()
            return resp.json()["choices"][0]["message"]["content"].strip()
        except requests.exceptions.RequestException as e:
            time.sleep(2 ** attempt)
            print(f"  Error ({model}): {e}")
    return None


def ask_llm(messages, model=LLM_MODEL, max_retries=5, temperature=LLM_TEMPERATURE):
    result = _call_llm_api(messages, model, max_retries, temperature)
    if result is not None:
        return result
    for fb in ["openthaigpt", "kbtg", "pathumma"]:
        if fb == model:
            continue
        print(f"  >> Fallback: {fb}")
        result = _call_llm_api(messages, fb, max_retries=3, temperature=temperature)
        if result is not None:
            return result
    return None


def parse_answer(text):
    if text is None:
        return None
    clean = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL).strip()
    m = re.search(r"ANSWER:\s*(\d{1,2})", clean)
    if m:
        val = int(m.group(1))
        if 1 <= val <= 10:
            return val
    for d in re.findall(r"\b(\d{1,2})\b", clean):
        if 1 <= int(d) <= 10:
            return int(d)
    return None

