def chunk_by_markdown(text, metadata, chunk_size=CHUNK_SIZE, overlap=CHUNK_OVERLAP):
    parts = []
    if metadata.get("product"): parts.append(metadata["product"])
    if metadata.get("brand"): parts.append(metadata["brand"])
    if metadata.get("category"): parts.append(metadata["category"])
    prefix = f"[{' | '.join(parts)}]\n" if parts else ""
    sections = re.split(r"\n(?=##\s)", text)
    if len(sections) <= 1:
        sections = text.split("\n\n")
    chunks = []
    current = ""
    for sec in sections:
        sec = sec.strip()
        if not sec:
            continue
        if len(current) + len(sec) + 2 <= chunk_size:
            current = (current + "\n\n" + sec).strip()
        else:
            if current.strip():
                chunks.append(prefix + current.strip())
            if len(sec) <= chunk_size:
                current = sec
            else:
                paragraphs = sec.split("\n")
                sub = ""
                for p in paragraphs:
                    if len(sub) + len(p) + 1 <= chunk_size:
                        sub = (sub + "\n" + p).strip()
                    else:
                        if sub.strip():
                            chunks.append(prefix + sub.strip())
                        sub = p
                current = sub.strip() if sub.strip() else ""
    if current.strip():
        chunks.append(prefix + current.strip())
    if not chunks:
        chunks = [prefix + text.strip()]
    return chunks


