def majority_vote(answers):
    """Pick the answer that appears most. Tie → pick first occurrence."""
    valid = [a for a in answers if a is not None]
    if not valid:
        return 9
    counts = Counter(valid)
    # Most common answer
    winner, count = counts.most_common(1)[0]
    return winner


