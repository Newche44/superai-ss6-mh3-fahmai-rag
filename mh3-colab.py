def extract_core_question(query):
    if len(query) < 200: return query
    products = detect_products(query)
    markers = ["ไหม", "มั้ย", "ครับ", "คะ", "ค่ะ", "เท่าไหร่", "กี่", "อะไร",
               "ยังไง", "ได้มั้ย", "ได้ไหม", "อยากรู้", "อยากทราบ", "?"]
    sentences = re.split(r'[.?!。\n]+', query)
    sentences = [s.strip() for s in sentences if s.strip()]
    if len(sentences) <= 2: return query
    relevant = []
    for s in sentences:
        has_q = any(m in s for m in markers)
        has_p = any(p.lower() in s.lower() for p in products) if products else False
        if has_q or has_p: relevant.append(s)
    if relevant:
        core = " ".join(relevant)
        for p in products:
            if p.lower() not in core.lower(): core = f"{p} {core}"
        return core
    return " ".join(sentences[-2:])

